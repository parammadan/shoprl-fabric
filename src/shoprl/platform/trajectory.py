"""Trajectory schema + lineage (Pydantic, persisted).

A *trajectory* is the recorded episode that flows through the platform:
rollout produces it, reward scores it, the optimizer consumes it. Pillars 1-2
track the *job* (the unit of work); this pillar captures the *data that work
produced*, with enough provenance to answer "where did this training signal
come from?".

Why Pydantic (vs the plain dataclasses used for Job): trajectories are the data
that crosses process/queue/disk boundaries and gets replayed. Validating them
at the boundary — rewards finite, steps present and correctly indexed — catches
corrupt/garbage episodes before they poison a gradient, and gives free, stable
JSON (de)serialisation for persistence.

Lineage is the provenance chain: which policy version generated the episode,
which job created it, which prompt/seed, and which trajectory (if any) it was
derived from. This is what makes a bad result debuggable ("this KL spike came
from trajectories generated by policy step-007 under job J, seed 42") and lets
you reconstruct a derivation graph (re-scored / continued trajectories point at
their parent).

Scope: real, single-machine schema + provenance. Nothing simulated here.
"""
from __future__ import annotations

import math
import time
import uuid
from typing import Literal

from pydantic import BaseModel, Field, field_validator, model_validator


def _finite(name: str, v: float | None) -> float | None:
    if v is not None and not math.isfinite(v):
        raise ValueError(f"{name} must be finite, got {v}")
    return v


class TrajectoryStep(BaseModel):
    """One step of an episode. For a single-turn task there is exactly one
    step; for the multi-turn ShopEnv there is one per turn."""
    index: int = Field(ge=0)               # 0-based position within the episode
    action: str                            # raw text the policy emitted
    observation: str = ""                  # environment feedback after the action
    reward: float | None = None            # per-step reward, if the task gives one
    logprob: float | None = None           # sum log-prob of the action under the
                                           # sampling policy (for off-policy checks)

    @field_validator("reward", "logprob")
    @classmethod
    def _step_finite(cls, v, info):
        return _finite(info.field_name, v)


class Lineage(BaseModel):
    """Provenance for one trajectory."""
    policy_id: str                         # policy/checkpoint version that generated it
    job_id: str | None = None              # Pillar 1/2 job that produced it
    prompt_id: str | None = None           # which prompt/goal
    seed: int | None = None                # sampling seed (reproducibility)
    parent_id: str | None = None           # trajectory this was derived from (graph edge)


class Trajectory(BaseModel):
    id: str = Field(default_factory=lambda: uuid.uuid4().hex)
    kind: Literal["single_turn", "multi_turn"] = "multi_turn"
    prompt: str
    steps: list[TrajectoryStep]
    reward: float | None = None            # episode-level (final) reward
    lineage: Lineage
    created_at: float = Field(default_factory=time.time)
    meta: dict = Field(default_factory=dict)

    @field_validator("reward")
    @classmethod
    def _reward_finite(cls, v):
        return _finite("reward", v)

    @model_validator(mode="after")
    def _check_steps(self):
        if not self.steps:
            raise ValueError("a trajectory must have at least one step")
        # Steps must be contiguous 0..n-1: a gap/dup means a dropped or
        # duplicated turn, which would silently misalign credit assignment.
        expected = list(range(len(self.steps)))
        actual = [s.index for s in self.steps]
        if actual != expected:
            raise ValueError(f"step indices must be {expected}, got {actual}")
        return self

    @property
    def num_steps(self) -> int:
        return len(self.steps)

    @classmethod
    def from_completion(cls, completion, reward, lineage: Lineage,
                        prompt_id: str | None = None) -> "Trajectory":
        """Build a single-turn trajectory from a rollout.base.Completion."""
        if prompt_id is not None and lineage.prompt_id is None:
            lineage = lineage.model_copy(update={"prompt_id": prompt_id})
        return cls(
            kind="single_turn", prompt=completion.prompt, reward=reward,
            lineage=lineage,
            steps=[TrajectoryStep(index=0, action=completion.text, reward=reward)],
        )

    def derive(self, **lineage_overrides) -> "Trajectory":
        """Create a child trajectory whose lineage.parent_id points at this one
        (e.g. a re-scored copy). New id; parent linkage set automatically."""
        child_lineage = self.lineage.model_copy(
            update={"parent_id": self.id, **lineage_overrides})
        return self.model_copy(update={
            "id": uuid.uuid4().hex, "lineage": child_lineage,
            "created_at": time.time()})
